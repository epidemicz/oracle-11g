#!/bin/bash

EXCLUDE_CMD=
SCHEMA_REMAP=
TABLESPACE_REMAP=

if [ -n "$EXCLUDE" ]; then
    EXCLUDE_CMD="EXCLUDE=$EXCLUDE"
fi

if [ -n "$SOURCE_SCHEMA" ]; then
    SCHEMA_REMAP="REMAP_SCHEMA=$SOURCE_SCHEMA:localdev"
fi

if [ -n "$SOURCE_TABLESPACE" ]; then
    TABLESPACE_REMAP="REMAP_TABLESPACE=$SOURCE_TABLESPACE:users"
fi

impdp localdev/localdev \
FULL=Y \
DIRECTORY=DATA_PUMP_DIR \
DUMPFILE=dump.dmp \
LOGFILE=import.log \
EXCLUDE=SCHEMA:\"IN \(\'SYS\',\'SYSTEM\',\'APEX_030200\',\'SYSMAN\',\'OLAPSYS\',\'ORDDATA\'\)\"